/*
 * Энциклопедия «Вавилон» — встроенная база знаний (Академия).
 * Часть статей собирается из данных игры (статы, заклинания, миры, рецепты),
 * чтобы описание не расходилось с реальной механикой.
 * Локализация: вся проза и имена данных проходят через t().
 */

function encStripTags(html) { return String(html).replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim(); }

function buildEncyclopedia() {
  const E = [];
  const li = (arr) => `<ul class="enc-ul">${arr.map((x) => `<li>${x}</li>`).join('')}</ul>`;

  // 1. О мире
  E.push({ icon: '🌍', title: t('О мире и валютах'), body: `
    <p>${t('«Вавилон» — мир из двух частей, соединённых башней-цитаделью. Игроки приходят как полубоги.')}
    ${t('Нижний мир — мир смертных (стратегия и ресурсы), верхний — нестабильные локации (РПГ-походы). Вавилонская башня в центре — ваш дом и хаб.')}</p>
    <p>${t('Три особые валюты:')}</p>
    ${li([
      t('🪙 <b>Золото</b> — игровые деньги: покупки в магазине, торговля.'),
      t('👻 <b>Души</b> — премиум-валюта: покупаются за Telegram Stars (⭐) или выдаются за ключевые квесты. Меняются в Банке на Золото и Искры.'),
      t('🔥 <b>Искры</b> — «божественные искры» для крафта и заточки. Добываются в верхнем мире (походы): чем глубже тир локации, тем активнее сыплются.'),
    ])}
    <p>${t('💎 <b>Камни</b> (самоцветы для усилителей) тоже добываются в верхнем мире — выпадают с боёв, и тем чаще, чем глубже тир. В магазине есть, но дорого.')}</p>` });

  // 2. Статы
  E.push({ icon: '💪', title: t('Статы (9 характеристик)'), body: `
    <p>${t('У героя 9 характеристик. Каждая растёт от использования по геометрической прогрессии (планка 100 → 200 → 400…). Прогресс виден полосками в Покоях героя.')}</p>
    ${li(STAT_ORDER.map((k) => `<b>${t(STATS[k].name)}</b> — ${t(STATS[k].desc)} <i>(${t('растёт от:')} ${t(STATS[k].grows)})</i>`))}` });

  // 3. Уровень и опыт
  E.push({ icon: '🎖', title: t('Уровень, опыт и «опасность»'), body: `
    <p>${t('Есть две прокачки, и они дополняют друг друга:')}</p>
    ${li([
      t('<b>Уровень</b> — классический, растёт от опыта за бои (зелёная полоска в шапке). За новый уровень: полное восстановление HP/MP и награда искрами. Планка следующего уровня: 100 → 175 → 250…'),
      t('<b>Опасность</b> — боевая мощь героя, считается из суммы статов. Влияет на силу тренировочного двойника на Арене.'),
    ])}
    <p>${t('Опыт капает за убитых мобов (больше за старшие миры, ×3 за боссов).')}</p>` });

  // 4. Бой
  E.push({ icon: '⚔️', title: t('Бой'), body: `
    <p>${t('Бой пораундовый. Каждый раунд вы выбираете зону атаки и зону блока (голова, торс, руки, ноги), либо кастуете заклинание, либо пьёте эликсир, либо бежите.')}</p>
    ${li([
      t('Если враг бьёт в зону, которую вы заблокировали — урон режется.'),
      t('Возможны криты (Ярость — физ., Вера — маг.) и контрудары (Реакция — физ., Отражение — маг.).'),
      t('Удача повышает шанс макс. урона и ценных трофеев.'),
      t('Поражение не убивает насовсем — вы возвращаетесь в башню ослабленным (HP ~30%) и теряете 10% золота.'),
      t('Сложность локации (75–200%) — ваш регулятор челленджа. Высокие миры без прокачки непроходимы: качайтесь и улучшайте снаряжение.'),
    ])}
    <p>${t('Тренироваться без риска можно на Арене (бой с тёмным двойником).')}</p>` });

  // 5. Магия
  const byElem = {};
  ELEMENTS.forEach((el) => { byElem[el] = SPELLS.filter((s) => s.element === el); });
  E.push({ icon: '✨', title: t('Магия (4 стихии × 3 направления)'), body: `
    <p>${t('Стихии:')} <b>${ELEMENTS.map((e) => t(e)).join(', ')}</b>. ${t('Направления:')} <b>${DIRS.map((d) => t(d)).join(', ')}</b> ${t('(свет и тьма частично взаимоисключающие, сумрак нейтрален). Каст повышает уровень стихии, направления и Интеллект.')}</p>
    <p>${t('<b>Где смотреть свои заклинания:</b> Покои героя → блок «Магия». <b>Как применять:</b> только в бою — выпадающий список «Заклинание» → «✨ Каст». Новые заклинания (формулы) иногда выпадают трофеем из мобов.')}</p>
    ${ELEMENTS.map((el) => `<div class="enc-sub"><b>${t('Стихия')} «${t(el)}»:</b> ${li(byElem[el].map((s) => `<b>${t(s.name)}</b> (${t(s.dir)}, ${s.cost} MP) — ${t(s.desc)}`))}</div>`).join('')}` });

  // 6. Ресурсы
  const resByTier = (tr) => Object.values(RESOURCES).filter((r) => r.tier === tr).map((r) => `${r.icon} ${t(r.name)}`);
  E.push({ icon: '🪵', title: t('Ресурсы'), body: `
    <p>${t('Ресурсы делятся на уровни. 1 уровень добывается руками/в боях, 2 уровень производится в мастерских.')}</p>
    ${li([
      t('<b>1 уровень:</b>') + ' ' + resByTier(1).join(', '),
      t('<b>2 уровень:</b>') + ' ' + resByTier(2).join(', '),
      t('<b>Особые:</b> 🪙 Золото, 👻 Души, 🔥 Искры'),
    ])}
    <p>${t('Добыть ресурсы 1 уровня: Нижний мир → «Добыть руками» (бесплатно), пассивная добыча построек смертных, покупка за золото в Магазине; ещё они падают трофеями в боях.')}</p>` });

  // 7. Мастерские и топливо
  E.push({ icon: '🔨', title: t('Мастерские, крафт и топливо'), body: `
    <p>${t('В Мастерских ресурсы 1 уровня перерабатываются во 2 уровень, а из них создаются оружие, броня и бижутерия.')}</p>
    <p>${t('<b>Что такое топливо.</b> Это не отдельная кнопка, а ресурс — древесный уголь или брёвна. Оно тратится автоматически при создании вещей, которым нужна высокая температура. Правило:')}</p>
    ${li([
      t('<b>1 топливо = 1 древесный уголь</b> (даёт закалку, +качество предмета), <b>или</b>'),
      t('<b>1 топливо = 6 брёвен</b> (если угля нет; качество обычное).'),
    ])}
    <p>${t('<b>Где взять:</b> брёвна — Магазин → «Срубить дерево»; уголь — Мастерские → «Переработка ресурсов» → «Древесный уголь» (5 брёвен → 1 уголь).')}</p>
    <p>${t('<b>Цепочка для оружия/брони:</b> брёвна → (уголь) и руда → металл → сам предмет (металл + доска + искры + топливо).')}</p>` });

  // 8. Алхимия
  E.push({ icon: '⚗️', title: t('Алхимия (Лаборатории)'), body: `
    <p>${t('Алхимия делится на три вида:')}</p>
    ${li([
      t('<b>Эликсиры</b> — применяются в бою (восстанавливают HP или MP).'),
      t('<b>Зелья</b> — бросаются во врага, наносят урон.'),
      t('<b>Мази</b> — применяются вне боя, дают временные усиления.'),
    ])}
    <p>${t('Создаются в Лабораториях из трав, грибов и слюды. Эликсиры/зелья используются прямо в окне боя.')}</p>` });

  // 9. Снаряжение
  E.push({ icon: '🛡', title: t('Снаряжение'), body: `
    <p>${t('Слоты: оружие, шлем, доспех, щит, кольцо, амулет, серьги. Надеть вещь можно в Покоях героя (кнопка «надеть»), если хватает требуемых статов.')}</p>
    ${li([
      t('<b>Оружие</b> — урон, дистанция (ближняя/средняя/дальняя), одноручное/двуручное.'),
      t('<b>Броня</b> — лёгкая/средняя/тяжёлая, добавляет защиту и броню.'),
      t('<b>Бижутерия</b> — не даёт физзащиты, но усиливает статы (энергетический сосуд).'),
    ])}
    <p>${t('Уголь при ковке повышает качество (прочность/урон/защиту).')}</p>` });

  // 9b. Рарность, сеты и сборки
  E.push({ icon: '🎽', title: t('Рарность, классовые сеты и сборки'), body: `
    <p>${t('<b>Рарность.</b> Снаряжение бывает разной редкости — она множит характеристики предмета. Чем выше уровень героя и сложность похода, тем больше шанс высокой рарности (на 200% выпадают самые редкие).')}</p>
    ${li(RARITY_ORDER.map((k) => `<span style="color:${RARITIES[k].color}"><b>${t(RARITIES[k].name)}</b></span> — ×${RARITIES[k].mult.toFixed(2)} ${t('к статам')}`))}
    <p>${t('<b>Классовые сеты.</b> 7 комплектов по классам, каждый из 5 предметов. Части одного сета выпадают по отдельности и могут быть разной рарности.')}</p>
    ${li(Object.values(GEAR_SETS).map((s) => `${s.icon} <b>${t(s.name)}</b> — ${t(s.class)}`))}
    <p>${t('<b>Сет-бонусы.</b> За надетые части одного комплекта (2 / 4 / полный) даются бонусы к статам и боевым параметрам — независимо от рарности частей. Прогресс виден в Покоях героя и в разделе «Коллекция» (Кодекс).')}</p>
    <p>${t('<b>Где взять сетовые части.</b> Только два пути: выпадают в походах (дроп) или покупаются за Души в Магазине (вкладка «За души»). Ковки сетовых частей нет.')}</p>
    <p>${t('<b>Премиум-лавка</b> (Магазин → вкладка «За души»): готовые сетовые предметы можно купить за Души — по частям или целым комплектом (сетом дешевле). Рарность выбирается вкладкой.')}</p>
    <p>${t('<b>Сборки экипировки</b> (в Покоях героя): сохрани текущий комплект под именем и переключайся между сборками одним кликом — удобно держать, например, отдельные комплекты мага и воина.')}</p>` });

  // 10. Походы и миры
  E.push({ icon: '🪜', title: t('Походы и миры'), body: `
    <p>${t('Через «Лестницу в Небо» можно отправиться в один из 12 миров. У каждой локации — свои мобы и уровень сложности (75% — мобы слабее на 25%, 200% — вдвое сильнее).')}</p>
    <p>${t('Награда за победу: опыт, золото, искры, ресурсы-трофеи и иногда формулы заклинаний.')}</p>
    ${li(WORLDS.map((w, i) => `<b>${i + 1}. ${t(w.name)}</b> (${t('сложность')} ×${w.tier}) — ${t(encStripTags(w.intro))}`))}` });

  // 11. Бестиарий
  E.push({ icon: '🐉', title: t('Бестиарий (мобы по мирам)'), body: `
    <p>${t('Каждому мобу присваиваются свои параметры — даже одинаковые по названию твари из разных миров отличаются. Боссы (помечены ⭐) крупнее и дают лучший лут.')}</p>
    ${WORLDS.map((w) => { const mobs = [...new Set([].concat(...w.locations.map((l) => l[1])))]; return `<div class="enc-sub"><b>${t(w.name)}:</b> ${mobs.map((m) => t(m)).join(', ')}.</div>`; }).join('')}` });

  // 12. Квесты
  E.push({ icon: '📜', title: t('Квесты (Совет старейшин)'), body: `
    <p>${t('Задания берутся в Совете старейшин. У многих есть «градиент» — они повторяются с растущей целью (например, убить 10 / 100 / 1000 мобов). За каждый этап — награда.')}</p>
    ${li(QUESTS.map((q) => `<b>${t(q.name)}</b> — ${t(q.desc)}`))}` });

  // 13. Здания башни
  E.push({ icon: '🏯', title: t('Здания Вавилонской башни'), body: `
    ${li(TOWER_BUILDINGS.map((b) => `${b.icon} <b>${t(b.name)}</b> — ${t(b.desc)}`))}` });

  // 14. Банк и монетизация
  E.push({ icon: '🏛', title: t('Банк и монетизация'), body: `
    <p>${t('В Банке можно обменять Души (премиум-валюту) на Золото или Искры.')}</p>
    <p>${t('Души можно купить за Telegram Stars (⭐) прямо в Банке — это премиум-валюта, на которую затем берут премиум-предметы и услуги. Оплата проходит через Telegram, души зачисляются автоматически.')}</p>
    <p>${t('<b>Премиум-аккаунт</b> (в Банке): за Stars или Души на 30 дней. Даёт удобства, а не боевое преимущество: больше боёв на Арене с наградой в день, больше слотов под сборки экипировки и значок 👑 в чате и рейтинге.')}</p>
    <p>${t('Монетизация: паки Душ за Telegram Stars, премиум-предметы и сеты за Души в Магазине, премиум-аккаунт (за Stars или Души) и комиссия барахолки. Вывод дохода — на стороне владельца (Stars → TON через Fragment).')}</p>` });

  // 15. Питомцы
  E.push({ icon: '🐾', title: t('Питомцы (Приручение)'), body: `
    <p>${t('Ослабь дикого зверя (не босса) в бою примерно до 35% HP и нажми «🐾 приручить» — прирученный питомец будет помогать тебе в бою.')}</p>
    <p>${t('Навык <b>Приручение</b> повышает шанс поимки и число активных питомцев — по +1 за каждые 5 уровней навыка, максимум 3. Питомцы видны в Покоях героя на вкладке «Питомцы».')}</p>` });

  // 16. Усилители и огранка
  E.push({ icon: '💎', title: t('Усилители и огранка самоцветов'), body: `
    <p>${t('Из самоцветов (💎) огранкой создаются <b>усилители</b> — каждый даёт прибавку к одному стату. Огранка доступна в Покоях героя → Экипировка → блок «💎 Усилители (огранка)».')}</p>
    <p>${t('Снаряжение с гнёздами (💠) принимает усилители: вставь самоцвет в гнездо, чтобы добавить статов; усилитель можно вынуть обратно. Чем мощнее огранка, тем выше бонус и больше расход искр.')}</p>` });

  // 17. Профессии
  E.push({ icon: '🛠', title: t('Профессии и мастерство'), body: `
    <p>${t('У ремёсел есть уровни мастерства (Столяр, Кузнец, Алхимик и другие). Мастерство растёт от создания вещей и повышает их качество.')}</p>
    <p>${t('Рост уровня профессии открывает «тайные знания» — новые рецепты в мастерских и лабораториях. Текущие уровни видны в разделе «Профессии».')}</p>` });

  // 18. Гильдия магов
  E.push({ icon: '🔮', title: t('Гильдия магов'), body: `
    <p>${t('Клуб магов: вступление за разовый взнос. <b>Ранг</b> растёт с суммой уровней стихий (пороги 20 / 60 / 120).')}</p>
    <p>${t('В гильдии можно <b>улучшать</b> известные заклинания (до предела по рангу, +N к силе) за золото, искры и самоцветы, а также <b>изучать</b> новые заклинания, открытые по рангу.')}</p>` });

  // 19. Арена
  E.push({ icon: '🥋', title: t('Арена (тренировка)'), body: `
    <p>${t('На Арене можно сразиться с тёмным двойником ради опыта и золота — без риска потерять вещи. Сила двойника зависит от твоей «опасности».')}</p>
    <p>${t('Награда даётся за первые 25 боёв в день; сверх лимита бой засчитывается, но без золота и опыта.')}</p>` });

  // 20. Кланы
  E.push({ icon: '🛡️', title: t('Кланы'), body: `
    <p>${t('Клан — объединение до 20 игроков. Можно создать свой клан или вступить в открытый. У клана есть казна, улучшения, состав, журнал событий, чат и совместный рейд.')}</p>
    <p>${t('Казна (🪙) пополняется взносами участников и наградами рейда; из неё лидер оплачивает улучшения клана:')}</p>
    ${li(CLAN_UPGRADE_ORDER.map((k) => `${CLAN_UPGRADES[k].icon} <b>${t(CLAN_UPGRADES[k].name)}</b> — ${t(CLAN_UPGRADES[k].desc)}`))}
    <p>${t('<b>Бонус клана</b> даёт всем участникам прибавку ко всем статам: +1 за размер состава и ещё +1 за каждый уровень «Артефакта клана».')}</p>` });

  // 21. Рейд и арсенал
  E.push({ icon: '🐉', title: t('Клановый рейд и арсенал'), body: `
    <p>${t('<b>Рейд</b> — общий босс клана с единым пулом HP. Это полноценный интерактивный бой: бей босса сколько выдержишь за заход, нанесённый урон вычитается из общего пула.')}</p>
    <p>${t('Перезарядка 10 минут включается только если ты отступил или пал (босс выжил). Убил — сразу можно идти на следующего, более крепкого. После убийства награда (золото, искры, опыт) начисляется всем участникам и забирается в разделе «Рейд».')}</p>
    <p>${t('<b>Арсенал</b> — клановый прокат снаряжения. Одолжи свою вещь клану (📦 в инвентаре) или арендуй чужую. Арендованное можно только носить — владелец вправе забрать его в любой момент.')}</p>` });

  // 22. Чаты
  E.push({ icon: '💬', title: t('Чаты (мир и клан)'), body: `
    <p>${t('Чат мира — общий живой чат всех игроков. Вкладки: 🌐 «Глобальный» и по флажку на каждый язык; вкладка просто выбирает комнату, язык сообщений не проверяется. У клана есть отдельный чат.')}</p>
    <p>${t('<b>Модерация.</b> Мат запрещён: за нарушение — блокировка чата на час, после трёх нарушений — навсегда. Бан общий для мирового и кланового чатов.')}</p>` });

  // 23. Барахолка
  E.push({ icon: '🛒', title: t('Барахолка (торговля между игроками)'), body: `
    <p>${t('На Барахолке игроки продают друг другу вещи за золото: выставляй свои предметы на продажу или покупай чужие.')}</p>
    <p>${t('Лимит — 12 активных лотов на игрока, лот живёт 30 дней. С продажи удерживается комиссия 1%, остальное приходит продавцу.')}</p>
    <p>${t('Барахолка делится на две вкладки: «За монеты» (обычные вещи и ресурсы) и «За души» (сетовые предметы). За души комиссия выше — 10%.')}</p>` });

  // подготовим plaintext для поиска
  E.forEach((s) => { s.plain = encStripTags(s.title + ' ' + s.body).toLowerCase(); });
  return E;
}

// фильтрация по строке поиска без перерисовки всей страницы (не теряем фокус)
function encFilter(q) {
  q = (q || '').trim().toLowerCase();
  document.querySelectorAll('#encList .enc').forEach((el) => {
    const hit = !q || (el.dataset.text || '').includes(q);
    el.style.display = hit ? '' : 'none';
    if (q && hit) el.open = true; else if (!q) el.open = false;
  });
}
