/*
 * Энциклопедия «Вавилон» — встроенная база знаний (Академия).
 * Часть статей собирается из данных игры (статы, заклинания, миры, рецепты),
 * чтобы описание не расходилось с реальной механикой.
 */

function encStripTags(html) { return String(html).replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim(); }

function buildEncyclopedia() {
  const E = [];
  const li = (arr) => `<ul class="enc-ul">${arr.map((x) => `<li>${x}</li>`).join('')}</ul>`;

  // 1. О мире
  E.push({ icon: '🌍', title: 'О мире и валютах', body: `
    <p>«Вавилон» — мир из двух частей, соединённых башней-цитаделью. Игроки приходят как <b>полубоги</b>.
    Нижний мир — мир смертных (стратегия и ресурсы), верхний — нестабильные локации (РПГ-походы).
    Вавилонская башня в центре — ваш дом и хаб.</p>
    <p>Три особые валюты:</p>
    ${li([
      '🪙 <b>Золото</b> — игровые деньги: покупки в магазине, торговля.',
      '👻 <b>Души</b> — премиум-валюта (в полной версии — реальные средства). Меняются в Банке.',
      '🔥 <b>Искры</b> — «божественные искры» для создания вещей высокого уровня (крафт оружия/брони/бижутерии).',
    ])}` });

  // 2. Статы
  E.push({ icon: '💪', title: 'Статы (9 характеристик)', body: `
    <p>У героя 9 характеристик. Каждая растёт <b>от использования</b> по геометрической прогрессии
    (планка 100 → 200 → 400…). Прогресс виден полосками в Покоях героя.</p>
    ${li(STAT_ORDER.map((k) => `<b>${STATS[k].name}</b> — ${STATS[k].desc} <i>(растёт от: ${STATS[k].grows})</i>`))}` });

  // 3. Уровень и опыт
  E.push({ icon: '🎖', title: 'Уровень, опыт и «опасность»', body: `
    <p>Есть <b>две</b> прокачки, и они дополняют друг друга:</p>
    ${li([
      '<b>Уровень</b> — классический, растёт от <b>опыта</b> за бои (зелёная полоска в шапке). За новый уровень: полное восстановление HP/MP и награда искрами. Планка следующего уровня: 100 → 175 → 250…',
      '<b>Опасность</b> — боевая мощь героя, считается из суммы статов. Влияет на силу тренировочного двойника на Арене.',
    ])}
    <p>Опыт капает за убитых мобов (больше за старшие миры, ×3 за боссов).</p>` });

  // 4. Бой
  E.push({ icon: '⚔️', title: 'Бой', body: `
    <p>Бой пораундовый. Каждый раунд вы выбираете <b>зону атаки</b> и <b>зону блока</b> (голова, торс, руки, ноги),
    либо кастуете заклинание, либо пьёте эликсир, либо бежите.</p>
    ${li([
      'Если враг бьёт в зону, которую вы заблокировали — урон режется.',
      'Возможны <b>криты</b> (Ярость — физ., Вера — маг.) и <b>контрудары</b> (Реакция — физ., Отражение — маг.).',
      'Удача повышает шанс макс. урона и ценных трофеев.',
      'Поражение не убивает насовсем — вы возвращаетесь в башню ослабленным (HP ~30%) и теряете 10% золота.',
      'Сложность локации (75–200%) — ваш регулятор челленджа. Высокие миры без прокачки непроходимы: качайтесь и улучшайте снаряжение.',
    ])}
    <p>Тренироваться без риска можно на <b>Арене</b> (бой с тёмным двойником).</p>` });

  // 5. Магия
  const byElem = {};
  ELEMENTS.forEach((el) => { byElem[el] = SPELLS.filter((s) => s.element === el); });
  E.push({ icon: '✨', title: 'Магия (4 стихии × 3 направления)', body: `
    <p>Стихии: <b>${ELEMENTS.join(', ')}</b>. Направления: <b>${DIRS.join(', ')}</b> (свет и тьма частично
    взаимоисключающие, сумрак нейтрален). Каст повышает уровень стихии, направления и Интеллект.</p>
    <p><b>Где смотреть свои заклинания:</b> Покои героя → блок «Магия». <b>Как применять:</b> только в бою —
    выпадающий список «Заклинание» → «✨ Каст». Новые заклинания (формулы) иногда выпадают трофеем из мобов.</p>
    ${ELEMENTS.map((el) => `<div class="enc-sub"><b>Стихия «${el}»:</b> ${li(byElem[el].map((s) => `<b>${s.name}</b> (${s.dir}, ${s.cost} MP) — ${s.desc}`))}</div>`).join('')}` });

  // 6. Ресурсы
  const resByTier = (t) => Object.values(RESOURCES).filter((r) => r.tier === t).map((r) => `${r.icon} ${r.name}`);
  E.push({ icon: '🪵', title: 'Ресурсы', body: `
    <p>Ресурсы делятся на уровни. 1 уровень добывается руками/в боях, 2 уровень производится в мастерских.</p>
    ${li([
      '<b>1 уровень:</b> ' + resByTier(1).join(', '),
      '<b>2 уровень:</b> ' + resByTier(2).join(', '),
      '<b>Особые:</b> 🪙 Золото, 👻 Души, 🔥 Искры',
    ])}
    <p>Добыть ресурсы 1 уровня: Магазин → «Добыть руками» (бесплатно) или покупка за золото; ещё они падают трофеями в боях.</p>` });

  // 7. Мастерские и топливо
  E.push({ icon: '🔨', title: 'Мастерские, крафт и топливо', body: `
    <p>В Мастерских ресурсы 1 уровня перерабатываются во 2 уровень, а из них создаются оружие, броня и бижутерия.</p>
    <p><b>Что такое топливо.</b> Это не отдельная кнопка, а ресурс — <b>древесный уголь или брёвна</b>. Оно тратится
    автоматически при создании вещей, которым нужна высокая температура. Правило:</p>
    ${li([
      '<b>1 топливо = 1 древесный уголь</b> (даёт закалку, +качество предмета), <b>или</b>',
      '<b>1 топливо = 6 брёвен</b> (если угля нет; качество обычное).',
    ])}
    <p><b>Где взять:</b> брёвна — Магазин → «Срубить дерево»; уголь — Мастерские → «Переработка ресурсов» →
    «Древесный уголь» (5 брёвен → 1 уголь).</p>
    <p><b>Цепочка для оружия/брони:</b> брёвна → (уголь) и руда → металл → сам предмет (металл + доска + искры + топливо).</p>` });

  // 8. Алхимия
  E.push({ icon: '⚗️', title: 'Алхимия (Лаборатории)', body: `
    <p>Алхимия делится на три вида:</p>
    ${li([
      '<b>Эликсиры</b> — применяются в бою (восстанавливают HP или MP).',
      '<b>Зелья</b> — бросаются во врага, наносят урон.',
      '<b>Мази</b> — применяются вне боя, дают временные усиления.',
    ])}
    <p>Создаются в Лабораториях из трав, грибов и слюды. Эликсиры/зелья используются прямо в окне боя.</p>` });

  // 9. Снаряжение
  E.push({ icon: '🛡', title: 'Снаряжение', body: `
    <p>Слоты: оружие, шлем, доспех, щит, кольцо, амулет, серьги. Надеть вещь можно в Покоях героя
    (кнопка «надеть»), если хватает требуемых статов.</p>
    ${li([
      '<b>Оружие</b> — урон, дистанция (ближняя/средняя/дальняя), одноручное/двуручное.',
      '<b>Броня</b> — лёгкая/средняя/тяжёлая, добавляет защиту и броню.',
      '<b>Бижутерия</b> — не даёт физзащиты, но усиливает статы (энергетический сосуд).',
    ])}
    <p>Уголь при ковке повышает качество (прочность/урон/защиту).</p>` });

  // 10. Походы и миры
  E.push({ icon: '🪜', title: 'Походы и миры', body: `
    <p>Через «Лестницу в Небо» можно отправиться в один из 12 миров. У каждой локации — свои мобы и
    уровень сложности (75% — мобы слабее на 25%, 200% — вдвое сильнее).</p>
    <p>Награда за победу: опыт, золото, искры, ресурсы-трофеи и иногда формулы заклинаний.</p>
    ${li(WORLDS.map((w, i) => `<b>${i + 1}. ${w.name}</b> (сложность ×${w.tier}) — ${encStripTags(w.intro)}`))}` });

  // 11. Бестиарий
  E.push({ icon: '🐉', title: 'Бестиарий (мобы по мирам)', body: `
    <p>Каждому мобу присваиваются свои параметры — даже одинаковые по названию твари из разных миров отличаются.
    Боссы (помечены ⭐) крупнее и дают лучший лут.</p>
    ${WORLDS.map((w) => { const mobs = [...new Set([].concat(...w.locations.map((l) => l[1])))]; return `<div class="enc-sub"><b>${w.name}:</b> ${mobs.join(', ')}.</div>`; }).join('')}` });

  // 12. Квесты
  E.push({ icon: '📜', title: 'Квесты (Совет старейшин)', body: `
    <p>Задания берутся в Совете старейшин. У многих есть «градиент» — они повторяются с растущей целью
    (например, убить 10 / 100 / 1000 мобов). За каждый этап — награда.</p>
    ${li(QUESTS.map((q) => `<b>${q.name}</b> — ${q.desc}`))}` });

  // 13. Здания башни
  E.push({ icon: '🏯', title: 'Здания Вавилонской башни', body: `
    ${li(TOWER_BUILDINGS.map((b) => `${b.icon} <b>${b.name}</b> — ${b.desc}`))}` });

  // 14. Банк и монетизация
  E.push({ icon: '🏛', title: 'Банк и монетизация', body: `
    <p>В Банке можно обменять Души (премиум-валюту) на Золото или Искры.</p>
    <p>По задумке GDD монетизация — это премиум-аккаунт, благословения, артефакты, кланы/семьи, барахолка,
    налоги и комиссии, а главная фишка — свободный ввод и вывод средств. В прототипе из этого реализован
    обмен Душ; остальное — на будущее.</p>` });

  // подготовим plaintext для поиска
  E.forEach((s) => { s.plain = encStripTags(s.title + ' ' + s.body).toLowerCase(); });
  return E;
}

// фильтрация по строке поиска без перерисовки всей страницы (не теряем фокус)
function encFilter(q) {
  q = (q || '').trim().toLowerCase();
  document.querySelectorAll('#encList .enc').forEach((el) => {
    const hit = !q || (el.dataset.text || '').includes(q);
    el.style.display = hit ? '' : 'none';
    if (q && hit) el.open = true; else if (!q) el.open = false;
  });
}
